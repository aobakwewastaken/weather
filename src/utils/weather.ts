export const getWeatherBackground = (weatherMain: string | undefined) => {
  switch (weatherMain?.toLowerCase()) {
    case "clear":
      return "from-[#1e3a8a] to-[#3b82f6]"; // Blue sky
    case "clouds":
      return "from-[#475569] to-[#94a3b8]"; // Cloudy
    case "rain":
    case "drizzle":
      return "from-[#1e3a8a] to-[#64748b]"; // Rainy
    case "thunderstorm":
      return "from-[#1e1b4b] to-[#312e81]"; // Stormy
    case "snow":
      return "from-[#e2e8f0] to-[#94a3b8]"; // Snowy
    case "mist":
    case "fog":
      return "from-[#475569] to-[#64748b]"; // Foggy
    default:
      return "from-[#2e026d] to-[#15162c]"; // Default purple
  }
};

